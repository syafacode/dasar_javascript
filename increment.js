//increment using ++
function increment() {
    var x = 0;
    console.log(x);
    x++;
    console.log(x);
    x++;
    console.log(x);
}

//decrement using --
function decrement(){
    var x = 10;
    console.log(x);
    x--;
    console.log(x);
    x--;
    console.log(x);
}

console.log("Increment");
increment();
console.log("Decrement");
decrement();